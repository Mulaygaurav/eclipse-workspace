package mvc;

public class Employee {

	private Employee empid = new Employee();
	private Employee name = new Employee();
	private Employee deptid = new Employee();
	
	public Employee getEmpid() {
		return empid;
	}
	public void setEmpid(Employee empid) {
		this.empid = empid;
	}
	public Employee getName() {
		return name;
	}
	public void setName(Employee name) {
		this.name = name;
	}
	public Employee getDeptid() {
		return deptid;
	}
	public void setDeptid(Employee deptid) {
		this.deptid = deptid;
	}
	
	
	
	
}
